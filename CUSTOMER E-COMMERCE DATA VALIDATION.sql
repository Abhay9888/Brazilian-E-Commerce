-- VAILIDATING  NEW CUSTOMER CURRENT  VS  LAST YEAR

WITH MAX_DATE_CTE AS 
( SELECT MAX(PURCHASE_DATE) AS MAX_DATE
FROM FACT_SALES
),

FIRST_PURCHASE AS (
SELECT CUSTOMER_UNIQUE_ID, MIN(PURCHASE_DATE) AS FIRST_PURCHASE_DATE
FROM FACT_SALES
WHERE ORDER_STATUS IN
    (
        'Approved',
        'Delivered',
        'Invoiced',
        'Processing',
        'Shipped',
        'Unavailable'
    )
GROUP BY CUSTOMER_UNIQUE_ID
),

CUST AS
(SELECT
COUNT (*) FILTER(
WHERE FIRST_PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE)
AND FIRST_PURCHASE_DATE <= M.MAX_DATE
) AS NEW_CUST,

COUNT (*) FILTER(
WHERE FIRST_PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE) - INTERVAL '1 YEAR'
AND FIRST_PURCHASE_DATE < DATE_TRUNC('YEAR', M.MAX_DATE)
) AS PREV_CUSTOMER

FROM FIRST_PURCHASE FP
CROSS JOIN MAX_DATE_CTE M
)
SELECT 
ROUND(((NEW_CUST - PREV_CUSTOMER):: NUMERIC / NULLIF(PREV_CUSTOMER,0)) * 100, 2)
AS GROWTH,
NEW_CUST,
PREV_CUSTOMER
FROM CUST;


--- VAILIDATING  REPEATED  CUSTOMER 

WITH MAX_DATE_CTE AS (
SELECT MAX(PURCHASE_DATE) AS MAX_DATE
FROM FACT_SALES),

FIRST_PURCHASE AS
(
SELECT CUSTOMER_UNIQUE_ID, MIN(PURCHASE_DATE) AS FIRST_PURCHASE_DATE
FROM FACT_SALES
 WHERE ORDER_STATUS IN
    (
        'Approved',
        'Delivered',
        'Invoiced',
        'Processing',
        'Shipped',
        'Unavailable'
    )
GROUP BY CUSTOMER_UNIQUE_ID
),

REPEATED AS 
(
SELECT
COUNT(DISTINCT FS.CUSTOMER_UNIQUE_ID) FILTER
(
WHERE FP.FIRST_PURCHASE_DATE < DATE_TRUNC('YEAR', M.MAX_DATE)
AND FS.PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE)
AND FS.PURCHASE_DATE <= M.MAX_DATE
) AS CURRENT_YEAR,


COUNT(DISTINCT FS.CUSTOMER_UNIQUE_ID) FILTER
(
WHERE FP.FIRST_PURCHASE_DATE < DATE_TRUNC('YEAR', M.MAX_DATE) -INTERVAL '1 YEAR'
AND FS.PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE) -INTERVAL '1 YEAR'
AND FS.PURCHASE_DATE <=  DATE_TRUNC('YEAR', M.MAX_DATE)
) AS LAST_YEAR

FROM FACT_SALES FS
CROSS JOIN MAX_DATE_CTE M
JOIN FIRST_PURCHASE FP
ON FS.CUSTOMER_UNIQUE_ID = FP.CUSTOMER_UNIQUE_ID

 WHERE FS.ORDER_STATUS IN
    (
        'Approved',
        'Delivered',
        'Invoiced',
        'Processing',
        'Shipped',
        'Unavailable'
    )
)

SELECT 
CURRENT_YEAR,
LAST_YEAR,
ROUND(((CURRENT_YEAR - LAST_YEAR)::NUMERIC / NULLIF(LAST_YEAR,0)) * 100, 2)
AS GROWTH
FROM REPEATED;


