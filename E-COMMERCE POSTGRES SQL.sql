WITH customer_first_purchase AS (
    SELECT
        customer_UNIQUE_id,
        MIN(purchase_date)::date AS first_purchase_date
    FROM FACT_SALES
    GROUP BY customer_UNIQUE_id
),

customer_yearly_purchase AS (
    SELECT DISTINCT
        customer_UNIQUE_id,
        EXTRACT(YEAR FROM purchase_date)::int AS purchase_year
    FROM FACT_SALES
)

SELECT
    cyp.purchase_year,

    COUNT(DISTINCT CASE
        WHEN EXTRACT(YEAR FROM cfp.first_purchase_date)::int = cyp.purchase_year
        THEN cyp.customer_UNIQUE_id
    END) AS new_customers,

    COUNT(DISTINCT CASE
        WHEN EXTRACT(YEAR FROM cfp.first_purchase_date)::int < cyp.purchase_year
        THEN cyp.customer_UNIQUE_id
    END) AS repeat_customers,

    COUNT(DISTINCT cyp.customer_UNIQUE_id) AS total_customers

FROM customer_yearly_purchase cyp
JOIN customer_first_purchase cfp
    ON cyp.customer_UNIQUE_id = cfp.customer_UNIQUE_id

GROUP BY cyp.purchase_year
ORDER BY cyp.purchase_year;

