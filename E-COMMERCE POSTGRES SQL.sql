SELECT * FROM ORDERS_ITEMS

CREATE VIEW FACT_ORDERS_ITEMS AS
SELECT
OI.ORDER_ID,  OI.PRODUCT_ID, OI.SHIPPING_LIMIT_DATE, OI.SELLER_ID,
SUM(OI.PRICE) AS TOTAL_REVENUE,
SUM(OI.FREIGHT_VALUE) AS FREIGHT_CHARGE,
MAX(OI.ORDER_ITEM_ID) AS TOTAL_ITEMS,
SUM(OI.PRICE + OI.FREIGHT_VALUE) AS TOTAL_PAYMENT
FROM ORDERS_ITEMS OI
GROUP BY OI.ORDER_ID, OI.PRODUCT_ID, OI.SHIPPING_LIMIT_DATE,  SELLER_ID

CREATE VIEW DIM_PRODUCT AS 
SELECT P.PRODUCT_ID, T.PRODUCT_CATEGORY,  T.CATEGORY_GROUP,
P.PRODUCT_WEIGHT_G, P.PRODUCT_LENGTH_CM, P.PRODUCT_HEIGHT_CM, P.PRODUCT_WIDTH_CM,
(P.PRODUCT_LENGTH_CM * P.PRODUCT_HEIGHT_CM * P.PRODUCT_WIDTH_CM ) AS VOLUME
FROM PRODUCTS P
LEFT JOIN CATEGORY T
ON T.PRODUCT_CATEGORY_NAME = P.PRODUCT_CATEGORY_NAME;

CREATE VIEW ORDERS_PAYMENT AS
SELECT
OP.ORDER_ID,
MAX(OP.PAYMENT_INSTALLMENTS) AS ORDERS_PAYMENT_INSTALLMENTS,
(ARRAY_AGG(OP.PAYMENT_TYPE ORDER BY OP.PAYMENT_VALUE DESC NULLS LAST))[1] AS ORDER_PAYMENT_TYPE
FROM PAYMENTS OP
GROUP BY OP.ORDER_ID; 



CREATE VIEW FACT_REVIEW AS 
SELECT DISTINCT ON (ORDER_ID)
ORDER_ID,
REVIEW_SCORE,
REVIEW_ID
FROM REVIEWS
ORDER BY ORDER_ID, REVIEW_CREATION_DATE DESC NULLS LAST;


CREATE VIEW FACT_ORDERS AS
SELECT
O.ORDER_ID,
O.CUSTOMER_ID,
C.CUSTOMER_UNIQUE_ID,
O.ORDER_STATUS,
O.ORDER_PURCHASE_TIMESTAMP AS PURCHASE_DATE,
O.ORDER_DELIVERED_CUSTOMER_DATE AS DELIVERED_DATE,
O.ORDER_ESTIMATED_DELIVERY_DATE AS ESTIMATED_DATE,
C.CUSTOMER_CITY,
C.CUSTOMER_STATE
FROM ORDERS O
JOIN CUSTOMERS C
ON C.CUSTOMER_ID = O.CUSTOMER_ID

CREATE VIEW FACT_SALES AS
SELECT 
OI.ORDER_ID,
OI.PRODUCT_ID,
OI.SELLER_ID,
O.CUSTOMER_UNIQUE_ID,
COALESCE(C.CATEGORY_GROUP,'N/A') AS CATEGORY_GROUP,
OI.SHIPPING_LIMIT_DATE,
OI.TOTAL_REVENUE,
OI.FREIGHT_CHARGE,
P.ORDER_PAYMENT_TYPE,
P.ORDERS_PAYMENT_INSTALLMENTS,
O.ORDER_STATUS,
O.PURCHASE_DATE,
O.DELIVERED_DATE,
O.ESTIMATED_DATE,
(O.ESTIMATED_DATE - O.DELIVERED_DATE) AS TOTAL_DAYS,
O.CUSTOMER_CITY,
O.CUSTOMER_STATE,
S.SELLER_CITY,
S.SELLER_STATE,
R.REVIEW_SCORE
FROM FACT_ORDERS_ITEMS OI
JOIN FACT_ORDERS O ON O.ORDER_ID = OI.ORDER_ID 
JOIN ORDERS_PAYMENT P ON OI.ORDER_ID = P.ORDER_ID
JOIN SELLERS S ON OI.SELLER_ID = S.SELLER_ID
JOIN REVIEWS R ON OI.ORDER_ID = R.ORDER_ID 
JOIN DIM_PRODUCT C ON OI.PRODUCT_ID = C.PRODUCT_ID


