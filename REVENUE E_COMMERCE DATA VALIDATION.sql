(SELECT MAX(PURCHASE_DATE) AS MAX_DATE FROM FACT_SALES
),

ITEM_SOLD AS (SELECT COUNT(ORDER_ID) FILTER(
WHERE PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE)
AND PURCHASE_DATE <= M.MAX_DATE
) AS CURRENT_ITEM_SOLD,

COUNT(ORDER_ID) FILTER(
WHERE PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE) -INTERVAL '1 YEAR'
AND PURCHASE_DATE <= DATE_TRUNC('YEAR', M.MAX_DATE)
) AS LY_ITEM_SOLD

FROM FACT_SALES FS 
CROSS JOIN MAX_DATE_CTE M
WHERE ORDER_STATUS IN 
('Approved','Delivered','Invoiced','Processing','Shipped','Unavailable')
)
SELECT 
  ROUND((CURRENT_ITEM_SOLD - LY_ITEM_SOLD)::NUMERIC / NULLIF(LY_ITEM_SOLD, 0 ) * 100, 2 ) AS GROWTH,
CURRENT_ITEM_SOLD ,
LY_ITEM_SOLD
FROM ITEM_SOLD

