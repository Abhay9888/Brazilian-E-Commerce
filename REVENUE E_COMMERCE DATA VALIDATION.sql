-- VALIDATING  YTD  LY REVENUE AND  GROWTH

WITH MAX_DATE_CTE AS (
SELECT MAX(PURCHASE_DATE) AS MAX_DATE
FROM FACT_SALES
),

REVENUE_CTE AS (SELECT 
SUM(TOTAL_REVENUE) FILTER(
WHERE PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE)
AND PURCHASE_DATE < M.MAX_DATE
) AS YTD_REVENUE,

SUM(TOTAL_REVENUE) FILTER(
WHERE PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE) - INTERVAL '1 YEAR'
AND PURCHASE_DATE < DATE_TRUNC('YEAR', M.MAX_DATE)
) AS LY_REVENUE

FROM FACT_SALES FS
CROSS JOIN MAX_DATE_CTE M
WHERE ORDER_STATUS IN 
('Approved','Delivered','Invoiced','Processing','Shipped','Unavailable')

)

SELECT 
(YTD_REVENUE - LY_REVENUE)  / (LY_REVENUE) * 100 AS GROWTH_PERCENTAGE ,
YTD_REVENUE,
LY_REVENUE
FROM REVENUE_CTE;




-- VALIDATING  REVENUE FOR LAST 30 AND  60 DAYS  

WITH MAX_DATE_CTE  AS 
(SELECT MAX(PURCHASE_DATE) AS  MAX_DATE  
FROM FACT_SALES
),

REVENUE as (SELECT 
SUM(TOTAL_REVENUE) FILTER(
WHERE PURCHASE_DATE >= M.MAX_DATE -INTERVAL '30 DAYS'
AND PURCHASE_DATE < M.MAX_DATE) 
AS THIRTY_DAYS,

SUM(TOTAL_REVENUE) FILTER (
WHERE PURCHASE_DATE >= M.MAX_DATE -INTERVAL '60 DAYS'
AND PURCHASE_DATE <= M.MAX_DATE -INTERVAL '30 DAYS')
AS SIXTY_DAYS


FROM FACT_SALES FS 
CROSS JOIN MAX_DATE_CTE M 
WHERE ORDER_STATUS IN 
('Approved','Delivered','Invoiced','Processing','Shipped','Unavailable')
)
SELECT 
ROUND((THIRTY_DAYS - SIXTY_DAYS) / SIXTY_DAYS * 100, 2) AS  GROWTH,
THIRTY_DAYS,
SIXTY_DAYS
FROM REVENUE


--- VALIDATING  GMV


WITH MAX_DATE_CTE  AS 
(SELECT MAX(PURCHASE_DATE) AS  MAX_DATE  
FROM FACT_SALES
),

GMV AS (SELECT 
SUM(TOTAL_REVENUE + FREIGHT_CHARGE) FILTER(
WHERE PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE)
AND PURCHASE_DATE <= M.MAX_DATE
) AS YTD_GMV,

SUM(TOTAL_REVENUE + FREIGHT_CHARGE)  FILTER(
WHERE PURCHASE_DATE >= DATE_TRUNC('YEAR', M.MAX_DATE) - INTERVAL ' 1 YEAR'
AND PURCHASE_DATE <= DATE_TRUNC('YEAR', M.MAX_DATE) 
) AS LY_GMV

FROM FACT_SALES FS 
CROSS JOIN MAX_DATE_CTE M 
WHERE ORDER_STATUS IN 
('Approved','Delivered','Invoiced','Processing','Shipped','Unavailable')
)
SELECT 
ROUND((YTD_GMV - LY_GMV) / LY_GMV * 100, 2) AS  GROWTH,
ROUND(YTD_GMV / 1000000, 2) AS YTD_GMV,
ROUND(LY_GMV / 1000000, 2) AS LY_GMV
FROM GMV


